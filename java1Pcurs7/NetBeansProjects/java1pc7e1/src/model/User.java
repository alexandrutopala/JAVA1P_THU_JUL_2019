package model;

import java.util.Objects;

/**
 *
 * @author student
 */
public class User {
    private String nume;
    private String varsta;

    public User(String nume, String varsta) {
        this.nume = nume;
        this.varsta = varsta;
    }

    public String getVarsta() {
        return varsta;
    }

    public void setVarsta(String varsta) {
        this.varsta = varsta;
    }

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 89 * hash + Objects.hashCode(this.nume);
        return hash;
    }
    

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final User other = (User) obj;
        if (!Objects.equals(this.nume, other.nume)) {
            return false;
        }
        return true;
    }
    
    
}
